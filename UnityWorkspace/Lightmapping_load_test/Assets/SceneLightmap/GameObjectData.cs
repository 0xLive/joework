using UnityEngine;
using System.Collections;
using System.Collections.Generic;

[System.Serializable]
public class GameObjectData
{
    public string m_name;

    public Vector3 m_position;

    public Vector3 m_rotation;

    public Vector3 m_scale;

    public int m_lightmapIndex;

    public Vector4 m_lightmapScaleOffset;

    public int m_realtimeLightmapIndex;

    public Vector4 m_realtimeLightmapScaleOffset;

    public void ToString()
    {
        Debug.Log(m_name + "|" + m_position + "|" + m_rotation + "|" + m_scale + "|" + m_lightmapIndex + "|" + m_lightmapScaleOffset + "|" + m_realtimeLightmapIndex + "|" + m_realtimeLightmapScaleOffset);
    }
}

